static int
roundTripTest(elzma_file_format format)
{
    int rc;
    unsigned char *compressed;
    unsigned char *decompressed;
    size_t sz;

    rc = simpleCompress(format, (unsigned char *)sampleData,
                        492, &compressed, &sz);

    if (rc != ELZMA_E_OK)
        return rc;

    /* gross assurance that compression is actually compressing */
    if (sz > 492)
    {
        free(compressed);
        return 1;
    }

    rc = simpleDecompress(format, compressed, sz,
                          &decompressed, &sz);

    free(compressed);

    if (rc != ELZMA_E_OK)
        return rc;

    if (sz != 492 || 0 != memcmp(decompressed, sampleData, sz))
    {
        free(decompressed);
        return 1;
    }

    return ELZMA_E_OK;
}

